-- ============================================================
-- 1. BOOKS TABLE
-- ============================================================
create table if not exists public.books (
  id            text primary key,
  title         text not null,
  emoji         text not null default '📘',
  level         text not null default 'مبتدئ',
  locked        boolean not null default false,
  coming_soon   boolean not null default false,
  sort_order    int  not null default 0,
  created_at    timestamptz not null default now(),
  updated_at    timestamptz not null default now()
);

-- ============================================================
-- 2. TEXTS TABLE
-- ============================================================
create table if not exists public.texts (
  id               text primary key,
  book_id          text not null references public.books(id) on delete cascade,
  title            text not null,
  page_num         int,
  description      text,
  display_icon     text default '📘',
  difficulty_level int  not null default 1 check (difficulty_level between 1 and 5),
  reading_level    text not null default 'مرحلة 1',
  game_available   boolean not null default false,
  sort_order       int  not null default 0,
  tags             jsonb not null default '[]',
  created_at       timestamptz not null default now(),
  updated_at       timestamptz not null default now()
);

-- ============================================================
-- 3. TEXT BODY TABLE  (individual sentences)
-- ============================================================
create table if not exists public.text_body (
  id           bigserial primary key,
  text_id      text not null references public.texts(id) on delete cascade,
  sentence     text not null,
  sort_order   int  not null default 0
);

-- ============================================================
-- 4. INDEXES
-- ============================================================
create index if not exists idx_texts_book_id       on public.texts(book_id);
create index if not exists idx_text_body_text_id   on public.text_body(text_id);
create index if not exists idx_texts_difficulty     on public.texts(difficulty_level);

-- ============================================================
-- 5. ROW LEVEL SECURITY
-- All content is publicly readable; only service_role can write.
-- ============================================================
alter table public.books     enable row level security;
alter table public.texts     enable row level security;
alter table public.text_body enable row level security;

-- Public read
create policy "books_public_read"     on public.books     for select using (true);
create policy "texts_public_read"     on public.texts     for select using (true);
create policy "text_body_public_read" on public.text_body for select using (true);

-- Admin write (authenticated users with admin role, or service_role key)
create policy "books_admin_write" on public.books
  for all using (auth.role() = 'service_role');
create policy "texts_admin_write" on public.texts
  for all using (auth.role() = 'service_role');
create policy "text_body_admin_write" on public.text_body
  for all using (auth.role() = 'service_role');

